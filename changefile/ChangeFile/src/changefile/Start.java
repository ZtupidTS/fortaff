        fileInputStream = new File(path);
        lastmodif = fileInputStream.lastModified();
        while(start)
        {
            //Pause for 4 seconds
            Thread.sleep(2000);
            fileInputStream = new File(path);
            if(lastmodif.equals(fileInputStream.lastModified()))
            {
                new Pop("BAD", Color.red);
            }else{
                new Pop("OK", Color.green);
                lastmodif = fileInputStream.lastModified();
            }            
        }
