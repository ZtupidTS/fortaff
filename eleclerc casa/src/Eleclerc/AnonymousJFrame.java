    /*
     * Permite preencher a combobox dos dados da BD
     * comboselect = section atual
     * selectable = se true pode se selecionar caso contrario não.
     */
    private void fillCombo(String comboselect, boolean selectable)
    {
        try {
            ResultSet rs = (ResultSet) Db.DbData.getDataTable("section", "", "", "", true);
            while (rs.next()) {
                    jCbxsection.addItem(rs.getString("sec_description"));
                }
            jCbxsection.setSelectedItem(comboselect);
            if(selectable)
            {
                jCbxsection.setEnabled(true);
            }            
        } catch (SQLException ex) {
            //Logger.getLogger(AnonymousJFrame.class.getName()).log(Level.SEVERE, null, ex);
            Messages.messageErrorDb("AnonymousJFrame : fillcombo");
        }
    }

