    private void jBtnCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCloseActionPerformed
        if(jTextAreaComments.getText().length() > 0)
        {
            if(Messages.messageQuestion("Pretende fechar sem submeter ?", "Aviso") != 0)
            {
                return;
            }
        }
        //caso seja anonimo fecha tudo
        //caso contrario fecha só a janela e volta a janela main
        if(user == 2)
        {
            DbConnect.dbDisconnect(this);
        }else{
            this.dispose();
        }        
    }//GEN-LAST:event_jBtnCloseActionPerformed

    private void jBtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSubmitActionPerformed
        if(jTextAreaComments.getText().length() > 0)
        {
            ArrayList field = new ArrayList();
            ArrayList column = new ArrayList();
            
            field.add(this.user);
            field.add(jTextAreaComments.getText());
            field.add(jCbxsection.getSelectedIndex() + 1);

            column.add("com_id_user");
            column.add("com_comments");
            column.add("com_id_section");

            if(DbData.insertData("comment", field, column) > 0)
            {
                //resposta da inserção na BD
                Messages.messageInformation("Sugestão Submetida, obrigado.", "Informação");

                //para enviar o mail
                EmailSender mail = new EmailSender();
                mail.sendEmail("Sugestão Submetida", user, jCbxsection.getSelectedItem().toString(),
                        jTextAreaComments.getText());

                //Desligo me da BD.
                if(user == 2)
                {
                    DbConnect.dbDisconnect(this);
                }else{
                    this.dispose();
                }
            }else{
                Messages.messageInformation("Informação", "Não foi possível Submeter,"
                        + "\n\r contacte o administrador");
            }
        }else{
            Messages.messageInformation("Problema", "Não escreveu nada");
        }
    }//GEN-LAST:event_jBtnSubmitActionPerformed


