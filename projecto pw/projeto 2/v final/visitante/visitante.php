	<div class="subtitulo"> Evolução dos Jogos Olímpicos</div>
	<div class="paragrafo">	
		<img src="images/jogos.jpg" class="imagem3"/>
		<p class="paragrafo1">
		&nbsp;&nbsp;&nbsp;&nbsp; A evolução do Movimento Olímpico durante o século XX obrigou o COI a adaptar os Jogos para o mundo da
		mudança das circunstâncias sociais. Alguns destes ajustes incluíram a criação dos Jogos de Inverno para
		esportes do gelo e da neve, os Jogos Paraolímpicos de atletas com deficiência física e os Jogos Olímpicos
		da Juventude para atletas adolescentes. O COI também teve de acomodar os Jogos para as diferentes variáveis econômicas, políticas e realidades tecnológicas do século XX. Como resultado, os Jogos Olímpicos se afastaram do amadorismo puro, como imaginado por Coubertin, para permitir a participação de atletas profissionais. A crescente importância dos meios de comunicação gerou a questão do patrocínio corporativo e a comercialização dos Jogos.
		O Movimento Olímpico é atualmente composto por federações esportivas internacionais, comitês olímpicos 
		nacionais (CONs) e comissões organizadoras de cada especificidade dos Jogos Olímpicos. Como o órgão de 
		decisão, o COI é responsável por escolher a cidade anfitriã para cada edição. A cidade anfitriã é
		responsável pela organização e financiamento à celebração dos Jogos coerentes com a Carta Olímpica.
		O programa olímpico, que consiste no esporte que será disputado a cada Jogos Olímpicos, também é determinado pelo COI. A celebração dos Jogos abrange muitos rituais e símbolos, como a tocha e a bandeira olímpica, bem como as cerimônias de abertura e encerramento. Existem mais de 13 000 atletas que competem nos Jogos Olímpicos de Inverno e em 33 diferentes modalidades esportivas com cerca de 400 eventos. Os finalistas do primeiro, segundo e terceiro lugar de cada evento recebem medalhas olímpicas de ouro, prata ou bronze, respectivamente.
		Os Jogos têm crescido em escala, a ponto de quase todas as nações serem representadas.
		Tal crescimento tem criado inúmeros desafios, incluindo boicotes, doping, corrupção de agentes públicos
		e terrorismo. A cada dois anos, os Jogos Olímpicos e sua exposição à mídia proporcionam a atletas
		desconhecidos a chance de alcançar fama nacional e, em casos especiais, a fama internacional. Os Jogos
		também constituem uma oportunidade importante para a cidade e o país se promover e mostrar-se para o
		mundo.
		</p>
