	<div class="paragrafo">	
		<img src="images/pierre.png" class="imagem2"/>
		<p class="paragrafo1"> &nbsp;&nbsp;&nbsp;&nbsp; No século XIX, o <b>Barão Pierre</b> de Coubertin fundou o Comitê Olímpico Internacional (COI)
		em <b>1894</b>. O COI se tornou o órgão dirigente do Movimento Olímpico, cuja estrutura e as ações são
		definidas pela Carta Olímpica.
		Foi também decidido que os primeiros Jogos Olímpicos da era moderna teriam lugar em Atenas, na Grécia
		e que a partir daí, tal como na antiguidade, seriam realizados a cada quatro anos (uma Olimpíada). 
		Dois anos depois realizaram-se os Jogos Olímpicos de Verão de 1896, que foram um sucesso.
		Após os Jogos de 1896, Demetrius Vikelas abandonou o posto de presidente do COI e Pierre de 
		Coubertin tomou o seu lugar na frente da organização. Apesar do sucesso dos primeiros jogos, o Movimento 
		Olímpico enfrentaria tempos difíceis, com os Jogos Olímpicos de 1900 e de 1904 a serem completamente obscurecidos 
		pelas exposições mundiais em que foram integrados, e passando completamente despercebidos.
		A situação melhorou com a realização dos Jogos Olímpicos de Verão de 1906 que, utilizando o pretexto
		de comemorar os 10 anos da primeira edição, serviram para limpar a imagem e promover os Jogos como um 
		evento internacional por excelência. <b>A partir de então os Jogos Olímpicos continuariam a ganhar audiência, 
		tornando-se o mais importante evento desportivo mundial</b>. Pierre de Coubertin abandonou a presidência do COI 
		após os Jogos Olímpicos de Verão de 1924, realizados em Paris, a sua cidade natal, e com um sucesso muito maior 
		que a anterior edição de 1900. Foi sucedido no cargo por Henri de Baillet-Latour.
		</p>
